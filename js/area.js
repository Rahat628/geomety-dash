// traingle rhombus pentagon area calculation
function HalfMultiArea(a,b){
    const area  = 0.5*a*b;
    return area;
}
// rectangle and parallelogram  area calculation
function FullMultiArea(a,b){
    const area  = a*b;
    return area;
}
// ellipse area calculation
function ellipseArea(a,b){
    const area = Math.PI *a*b;
    return area;
}
